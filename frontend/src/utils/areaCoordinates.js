// Helper function to get coordinates for a specific city and area
export const getAreaCoordinates = (city, area) => {
  if (!city || !area || !areaCoordinates[city]) {
    return null;
  }
  
  const cityAreas = areaCoordinates[city];
  const found = cityAreas.find(a => 
    a.name.toLowerCase() === area.toLowerCase() ||
    area.toLowerCase().includes(a.name.toLowerCase()) ||
    a.name.toLowerCase().includes(area.toLowerCase())
  );
  
  return found ? { lat: found.lat, lng: found.lng } : null;
};

// Get all areas for a specific city
export const getCityAreas = (city) => {
  return areaCoordinates[city] || [];
};

export default areaCoordinates;

